                     <p style="text-align: justify; font-size: 20px;">Jika Anda tidak berada di wilayah terjangkit COVID-19, atau jika Anda tidak melakukan perjalanan dari salah satu wilayah tersebut, atau tidak melakukan kontak dekat dengan seseorang yang memiliki gejala COVID-19 atau merasa kurang sehat, kecil kemungkinan Anda untuk tertular COVID19. Namun, dapat dimengerti bahwa Anda mungkin merasa stres dan cemas tentang situasi yang terjadi saat ini. Tetaplah tenang dan jangan panik. Carilah informasi yang benar dan akurat tentang perkembangan COVID-19 agar Anda mengetahui situasi wilayah Anda dan Anda dapat mengambil tindakan pencegahan yang wajar. Jika Anda berada di wilayah terjangkit COVID-19, Anda harus serius menghadapi risiko tersebut. Selalu jaga kesehatan dan perhatikan informasi dan saran dari pihak kesehatan yang berwenang.
